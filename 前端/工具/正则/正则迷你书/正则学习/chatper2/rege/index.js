module.exports = {
  // 3的倍数会导致首位被匹配到
  '数字千分位1': /(?=(\d{3})+$)/g,
  // 数字串如果有空格会出现问题
  '数字千分位2': /(?!^)(?=(\d{3})+$)/g,
  // 最终版
  '数字千分位3': /(?!\b)(?=(\d{3})+\b)/g,
  // 最终精简版
  '数字千分位4': /\B(?=(\d{3})+\b)/g,
  // 1888 => $ 1888.00
  '货币格式化': function (num){
    return num.toFixed(2).replace(/\B(?=(\d{3})+\b)/g,'.').replace(/^/,'$ ')
  },
  '密码验证1': /^[0-9a-zA-Z]{6,12}$/,
  '密码验证2': /(?=.*[0-9])^[0-9A-Za-z]{6,12}/
}